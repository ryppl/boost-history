      template <class RealType>
      RealType exponent_of_prime(RealType factorial, RealType prime) {
	RealType exponent=0;
	RealType temp=prime;
	do {
	  exponent+=factorial/prime;
	  prime*=temp;
	} while(prime<=factorial);
	return exponent;
      }

